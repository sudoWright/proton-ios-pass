import DesignSystem
import Entities
import UIKit

enum VaultColor: CaseIterable {
    case color1, color2, color3, color4, color5, color6, color7, color8, color9, color10

    var color: UIColor {
        switch self {
        case .color1:
            PassColor.vaultHeliotrope
        case .color2:
            PassColor.vaultMauvelous
        case .color3:
            PassColor.vaultMarigoldYellow
        case .color4:
            PassColor.vaultDeYork
        case .color5:
            PassColor.vaultJordyBlue
        case .color6:
            PassColor.vaultLavenderMagenta
        case .color7:
            PassColor.vaultChestnutRose
        case .color8:
            PassColor.vaultPorsche
        case .color9:
            PassColor.vaultMercury
        case .color10:
            PassColor.vaultWaterLeaf
        }
    }
}

extension ProtonPassVaultV1_VaultColor {
    var color: VaultColor {
        switch self {
        case .color1:
            .color1
        case .color2:
            .color2
        case .color3:
            .color3
        case .color4:
            .color4
        case .color5:
            .color5
        case .color6:
            .color6
        case .color7:
            .color7
        case .color8:
            .color8
        case .color9:
            .color9
        case .color10:
            .color10
        default:
            .color1
        }
    }
}
